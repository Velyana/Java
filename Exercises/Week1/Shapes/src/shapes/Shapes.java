// Lab 1: Shapes.java
// Program draws four shapes to the command window.
package shapes;

public class Shapes {

    public static void main(String args[]) {

        //draw square
        System.out.println("*****");
        System.out.println("*   *");
        System.out.println("*****\n");

        //draw rectangle
        System.out.println("********");
        System.out.println("*      *");
        System.out.println("********\n");

        //draw triangle
        System.out.println("     *     ");
        System.out.println("  *     *  ");
        System.out.println("***********\n");

        //draw rhomb
        System.out.println("     *     ");
        System.out.println("  *     *  ");
        System.out.println("*         *");
        System.out.println("  *     *  ");
        System.out.println("     *     ");

    }

} // end class Shapes

